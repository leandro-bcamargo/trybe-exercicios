// SCRUM

// Scrum é um framework para gestão de projetos. 

// Equipes divididas em product owner, scrum master e scrum team.

// As metodologias ágeis são iterativas, isto é, o trabalho é dividido em iterações (ciclos). No scrum, esses ciclos são chamados de SPRINTS.

// O sprint representa um espaço de tempo, geralmente de 2 a 4 semanas, em que um conjunto de atividades deve ser executado.

// As funcionalidades a serem implementadas são mantidas em uma lista chamada PRODUCT BACKLOG. COntém todas as funcionalidades elencadas como importantes para quem usa o produto.

// No início de cada sprint, faz-se um SPRINT PLANNING MEETING, uma reunião de planejamento na qual a pessoa PO prioriza os itens do product backlog e a equipe seleciona as atividades que ela será capaz de implementar durante o sprint que se inicia, originando o SPRINT BACKLOG.

// Lista de tarefas a serem desenvolvidas

// Especificação de cada tarefa, detalhando quais os critérios de aceite

// Alinhamento do time de como o desenvolvimento deve acontecer

// Na DAILY MEETING, cada pessoa deve responder a três perguntas:

// O que vocẽ fez ontem?

// O que vocẽ fará hoje?

// Há algum impedimento no seu caminho?

// Ao final de uma sprint, a equipe apresenta as funcionalidades em uma SPRINT REVIEW MEETING.

// Finalmente, faz-se uma SPRINT RETRO MEETING, onde são apontados os pontos positivos e negativos do último sprint, assim como apresentadas ações para mitigar os pontos negativos e evitar que se repitam. 

// Após essa reunião, a equipe parte para o planejamento do próximo sprint e o ciclo se reinicia.



// KANBAN

// A metodologia Kanban propõe a utilização de POST-ITS em um quadro para indicar e acompanhar de maneira visual, prática e utilizando poucos recursos, o andamento dos fluxos de produção em uma empresa.

// De um aldo do quadro, ficam as tarefas que precisam ser executadas, o chamado BACKLOG, que consiste em funcionalidades e objetivos do projeto como um todo, podendo ou não estarem completamente prontos nos processos iniciais de desenvolvimento. 

// Do outro lado, estão as etapas de execução: EM ANDAMENTO e ENTREGUE. 

// Conforme as tarefas são desempenhadas, o cartão post-it é colocado no campo correspondente ao status da tarefa.

// O Kanban permite que todas as pessoas tenham uma melhor apreciação do que as outras pessoas estão fazendo.
