const skills = (object) => {
  let skills = Object.keys(object);
  console.log(skills);
  let string = "";
  for (let i = 0; i < skills.length; i += 1) {
    string += `${skills[i]}, Nível: ${object[skills[i]]}, `;
  }
  return string;
}

