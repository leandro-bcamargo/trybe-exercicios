function createObjectFromArray(array) {
    let string = "";
    let counter = 0;
    let object = {};
    for (var i = 0; i < array.length; i += 1) {
        for (var j = 0; j < array.length; j += 1) {
            if (array[i] === array[j]) {
                counter += 1;
            }
        }
        object[array[i]] = counter;
        // string += counter, object[array[i]];
        counter = 0;
        
    }
    return object;
    // return "Sua cesta possui:", string;
}

